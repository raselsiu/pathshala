মুহাম্মদ (সাঃ) প্রথমে গোপনে এবং পরে প্রকাশ্যে তাওহীদের (এক আল্লাহর উপাসনা) বার্তা প্রচার শুরু করেন। 
তাঁর এই প্রচার কুরাইশ নেতাদের রোষানলে পড়ে। তাঁরা তাঁর বিরুদ্ধে নানা ষড়যন্ত্র করে এবং তাঁর অনুসারীদের ওপর অত্যাচার চালায়।

:::মদিনায় হিজরত:::
-------------------

১৩ বছর ধরে মক্কায় ইসলাম প্রচারের পর, ক্রমবর্ধমান নির্যাতনের কারণে তিনি মদিনায় হিজরত করেন। 
এটি ইসলামের ইতিহাসে একটি গুরুত্বপূর্ণ ঘটনা। 
মদিনায় তিনি ইসলামী রাষ্ট্র প্রতিষ্ঠা করেন এবং মুসলমানদের জন্য একটি ঐক্যবদ্ধ সমাজ গড়ে তোলেন।

:::যুদ্ধ ও শান্তি:::
-------------------

মদিনায় অবস্থানকালে তিনি মক্কার কুরাইশদের সাথে বদর, উহুদ এবং খন্দকসহ বিভিন্ন যুদ্ধ করেন। 
তবে তাঁর লক্ষ্য ছিল শান্তি প্রতিষ্ঠা। 
৬৩০ খ্রিস্টাব্দে তিনি মক্কা বিজয় করেন এবং কোনো প্রতিশোধ না নিয়ে সাধারণ ক্ষমা ঘোষণা করেন। 
এর ফলে মক্কার অধিকাংশ মানুষ ইসলাম গ্রহণ করে।

:::ইসলামের বিস্তার:::
-------------------

মুহাম্মদ (সাঃ) আরব উপদ্বীপে ইসলাম প্রতিষ্ঠা করেন এবং বিভিন্ন অঞ্চলের শাসকদের কাছে চিঠি লিখে ইসলামের বার্তা পৌঁছে দেন। 
তাঁর নেতৃত্বে ইসলাম শুধু একটি ধর্ম নয়, একটি পূর্ণাঙ্গ জীবনব্যবস্থা হিসেবে আত্মপ্রকাশ করে।

:::বিদায় হজ:::
-------------------

৬৩২ খ্রিস্টাব্দে বিদায় হজের সময় তিনি ১ লাখের বেশি সাহাবির সামনে একটি ঐতিহাসিক খুতবা প্রদান করেন। 
এতে তিনি মানবাধিকার, সাম্য এবং ইসলামের মৌলিক নীতিগুলো বর্ণনা করেন। 
এ হজের কিছুদিন পর তিনি অসুস্থ হয়ে পড়েন এবং ৬৩ বছর বয়সে মদিনায় ইন্তেকাল করেন।

